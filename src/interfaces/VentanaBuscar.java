    private void llenarTabla(List<Producto> lista) {
        int id = 1;
        for (int i = 0; i < lista.size(); i++) {
            fila[0] = id;
            fila[1] = lista.get(i).getClave();
            fila[2] = lista.get(i).getDescripcion();
            fila[3] = lista.get(i).getProveedor();
            fila[4] = lista.get(i).getCategoria();
            fila[5] = lista.get(i).getCantidad();
            fila[6] = "$ " + lista.get(i).getPrecio();
            fila[7] = "$ " + lista.get(i).getCosto();
            fila[8] = lista.get(i).getEstado();
            if (lista.get(i).getStatus().equals("1")) {
                fila[9] = "Activo";
            } else {
                fila[9] = "Inactivo";
            }
            modeloTabla.addRow(fila);
            id++;
        }
        System.out.println(lista.toString());
    }

    private void limpiarTable() {
        DefaultTableModel dftm = (DefaultTableModel) tablaProObtenidos.getModel();
        int a = tablaProObtenidos.getRowCount() - 1;

        for (int i = a; i >= 0; i--) {
            dftm.removeRow(dftm.getRowCount() - 1);
        }

    }

