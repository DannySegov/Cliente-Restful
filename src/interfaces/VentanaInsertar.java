    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        // TODO add your handling code here:
        try {
            Producto newProducto = new Producto();
            Consumeapi capi = new Consumeapi();
            Gson json = new Gson();

            newProducto.setClave(txtclave.getText());
            newProducto.setDescripcion(txtdescripcion.getText());
            newProducto.setCategoria(cbxcategoria.getItemAt(cbxcategoria.getSelectedIndex()));
            newProducto.setProveedor(txtproveedor.getText());
            newProducto.setCantidad(Integer.parseInt(cbxcantidad.getItemAt(cbxcantidad.getSelectedIndex())));
            newProducto.setCosto(Float.parseFloat(txtcosto.getText()));
            newProducto.setPrecio(Float.parseFloat(txtprecio.getText()));
            newProducto.setEstado(cbxestado.getItemAt(cbxestado.getSelectedIndex()));
            if(cbxstatus.getSelectedIndex()==0){
                newProducto.setStatus("1");
            }else{
                newProducto.setStatus("0");
            }
                

            String mandarProducto = json.toJson(newProducto);

            String respuestaDelServidor = capi.insertar(mandarProducto, String.class);

            if (!respuestaDelServidor.equals("error")) {
                JOptionPane.showMessageDialog(null, "Se agrego correctamente", "Metodo Agregar", 1);
            } else {
                JOptionPane.showMessageDialog(null, "Error al Agregar", "Metodo Agregar", 3);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_btnagregarActionPerformed


